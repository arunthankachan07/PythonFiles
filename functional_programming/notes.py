#functional programming
#lambda function
add=lambda num1,num2:num1+num2
print(add(10,20))
sub=lambda  num1,num2:num1-num2
print(sub(20,10))
div=lambda  num1,num2:num2/num1
print(div(20,10))
cube=lambda  num:num**3
print(cube(10))

check=lambda num:num%2==0  #check whether the number is even/odd
print(check(10))